<?php 

require_once 'Model.php';

class Admin extends Model{
    private $tableRow = "admins";
    //name email password status title description
    private $name;
    private $email;
    private $password_admin;
    private $status;
    private $title;
    private $description;

     public function __construct()
    {
        new Model($this->tableRow);

    }

    /**
     * @return mixed
     */
    public function getName()
    {
        return $this->name;
    }

    /**
     * @param mixed $name
     */
    public function setName($name)
    {
        $this->name = $name;
    }

    /**
     * @return mixed
     */
    public function getEmail()
    {
        return $this->email;
    }

    /**
     * @param mixed $email
     */
    public function setEmail($email)
    {
        $this->email = $email;
    }

    /**
     * @return mixed
     */
    public function getPasswordAdmin()
    {
        return $this->password_admin;
    }

    /**
     * @param mixed $password_admin
     */
    public function setPasswordAdmin($password_admin)
    {
        $this->password_admin = $password_admin;
    }

    /**
     * @return mixed
     */
    public function getStatus()
    {
        return $this->status;
    }

    /**
     * @param mixed $status
     */
    public function setStatus($status)
    {
        $this->status = $status;
    }

    /**
     * @return mixed
     */
    public function getTitle()
    {
        return $this->title;
    }

    /**
     * @param mixed $title
     */
    public function setTitle($title)
    {
        $this->title = $title;
    }

    /**
     * @return mixed
     */
    public function getDescription()
    {
        return $this->description;
    }

    /**
     * @param mixed $description
     */
    public function setDescription($description)
    {
        $this->description = $description;
    }




    

}